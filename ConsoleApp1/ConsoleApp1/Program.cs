using System;


class DeclareVariables
{
    static void Main()
    {
        ushort firstNumber = 52130;
        sbyte secondNumber = -115;
        uint thirdNumber = 4825932;
        byte fourthNumber = 97;
        short fifthNumber = -10000;
        Console.WriteLine(firstNumber + " " + "ushort");
        Console.WriteLine(secondNumber + " " + "sbyte");
        Console.WriteLine(thirdNumber + " " + "uint");
        Console.WriteLine(fourthNumber + " " + "byte");
        Console.WriteLine(fifthNumber + " " + "short");
        Console.ReadLine();
    }
}
